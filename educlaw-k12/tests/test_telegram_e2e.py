class TelegramBot:
    """Simulated Telegram bot — routes commands to db_query.py via subprocess.

    Command format:
        "Using {skill}, {action} with {key=value ...}"

    key names use underscores; they are converted to --hyphen-flags for argparse.
    The temp DB path from _S["db_path"] is always appended as --db-path.
    """

    def send(self, message: str) -> str:
        import re

        m = re.match(r"Using\s+([\w-]+),\s+(.+)", message, re.DOTALL)
        if not m:
            return json.dumps({"error": f"Cannot parse command: {message[:100]}"})

        skill = m.group(1)
        rest = m.group(2).strip()

        # Extract action name (first hyphen-separated token)
        action_m = re.match(r"([a-z][a-z0-9-]+(?:-[a-z0-9]+)*)", rest)
        if not action_m:
            return json.dumps({"error": f"Cannot parse action: {rest[:100]}"})

        action = action_m.group(1)
        args_str = rest[action_m.end():].strip()

        # Find db_query.py for the skill
        script = os.path.join(REPO_ROOT, "scripts", "db_query.py")

        cmd = [sys.executable, script, "--action", action]

        # Parse key=value or key="quoted value" pairs
        # Keys use underscores → convert to --hyphens for argparse
        for kv in re.finditer(
            r"([\w-]+)\s*=\s*(?:\"([^\"]*)\"|'([^']*)'|([^\s,]+))", args_str
        ):
            key = kv.group(1).replace("_", "-")
            val = kv.group(2) or kv.group(3) or kv.group(4) or ""
            cmd.extend([f"--{key}", val])

        # Always inject the temp DB path
        if _S.get("db_path"):
            cmd.extend(["--db-path", _S["db_path"]])

        try:
            result = subprocess.run(
                cmd, capture_output=True, text=True, timeout=30
            )
            return result.stdout.strip() or result.stderr.strip() or "empty response"
        except FileNotFoundError:
            return json.dumps({"error": f"Script not found: {script}"})
        except subprocess.TimeoutExpired:
            return json.dumps({"error": "Command timed out (30s)"})


